package com.jsp.sac;

public class Car 
{
	private int id;
	private String cost;
	private String brandcolor;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCost() {
		return cost;
	}
	public void setCost(String cost) {
		this.cost = cost;
	}
	public String getBrandcolor() {
		return brandcolor;
	}
	public void setBrandcolor(String brandcolor) {
		this.brandcolor = brandcolor;
	}
	
	

}
